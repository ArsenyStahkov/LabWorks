// Очистка данных
bool DataCleaning()
{
	bool clear = false;

	fstream buf("Buffer.txt", ios::out);

	if (!buf)
	{
		cout << "Ошибка открытия буферного файла!\n";
	}

	buf.clear();

	if (buf.peek() == EOF)
	{
		clear = true;
	}
	else
	{
		clear = false;
	}

	buf.close();

	return clear;
}

